def apply_personality_filters(response_text):
    """
    Final filter to ensure response maintains Mo's authentic voice.
    Checks for:
    - Over-corporate language (remove)
    - Generic sales-speak (replace with Mo phrases)
    - Missing specificity (add actual numbers where relevant)
    - Insufficient emotional register (boost if needed)
    
    Returns optimized response text.
    """
    filters = {
        "excellent value": "steal / great deal at this price",
        "limited supply": "I got [X] of these",
        "highly recommend": "you got to get this",
        "don't hesitate": "either you going to take it or you going to lose it",
        "we appreciate your business": "my boy [name], appreciate you",
        "in my opinion": "[I/we] know because [specific reason]",
        "affordable pricing": "get your money up and get real quality",
        "feel free to contact us": "hit the DM, submit a request",
    }
    
    optimized = response_text
    for corporate, authentic in filters.items():
        if corporate.lower() in optimized.lower():
            optimized = optimized.replace(corporate, authentic)
    
    return optimized


